public class For {
    public static void main(String[] args) throws Exception {
        //Para, permite que uma variável contadora seja testada e incrementada a cada iteração.

        for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {
            System.out.println("Contando carneirinhos " + carneirinhos + "º carneirinho");
        }

        System.out.println("O bebê dormiu");
    }
}