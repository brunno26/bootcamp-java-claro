package edu.bruno.anatomia;

public class Variaveis {
    
    public static void main(String[] args) {
        
    //Exemplo de varíaveis
    String meuNome = "Bruno";
    System.out.println(meuNome);

    int anoFabricação = 2024;
    System.out.println(anoFabricação);

    boolean verdadeira = true;
    System.out.println(verdadeira);

    }
}

//Toda variável deve ser escrita com letra minúscula, porém, se a palavra for composta a segunda palavra deverá ser com letra maiúscula: anoFabricação (CamelCase)

//A expressão final não permite mudar o valor de uma variável: final String BR = "Brasil"

